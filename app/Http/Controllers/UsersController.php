    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $admin_id = auth()->user()->id;
        //List all the users under the current admin
        $users =  User::orderBy('users.name','desc')->where('users.admin_id','=',$admin_id)
            ->leftjoin('policies','users.policy_id','=','policies.id')
            ->select('users.name','users.admin_id','users.email','users.phone','policies.name as policy')->get();
        return view('users.index')->with('users',$users);
    }

